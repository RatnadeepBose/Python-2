# MODULUS (%) - Remainder
print(10 % 3)   # 1 (10 ÷ 3 = 3 remainder 1)
print(15 % 4)   # 3 (15 ÷ 4 = 3 remainder 3)
print(7 % 5)    # 2 (7 ÷ 5 = 1 remainder 2)

# FLOOR DIVISION (//) - Quotient rounded DOWN
a, b = 10, 3
print(a // b)    # 3 (10 ÷ 3 = 3.33 → rounded down to 3)
print(-71 // 10) # -8 (-7.1 → rounded DOWN to -8, not -7)
print(15 // 4)   # 3 (15 ÷ 4 = 3.75 → 3)

# EXPONENTIATION (**) - Power
print(2 ** 3)    # 8 (2 × 2 × 2)
print(5 ** 4)    # 625 (5 × 5 × 5 × 5)
print(10 ** 2)   # 100 (10 squared)
print(3 ** 3)    # 27 (3 cubed)